a,b,c = map(int, input("Введите 3 целочисленных числа ЧЕРЕЗ ПРОБЕЛ..").split())
a1 = a*b
a2 = b*a
a3 = c*a
print("Операция умножения=", "a*b:", a1,
      "b*c:", a2,
      "c*a:", a3)
b1 = a**4
b2 = b%c
b3 = c//a
print("Возведение в степень, Остаток и Целое число=", "a**4:", b1,
      "b%c:", b2,
      "c//a:", b3)
summ = b1+b2+b3
print("Сумма числа в 4 степени, Остатка и Целого числа=", summ)